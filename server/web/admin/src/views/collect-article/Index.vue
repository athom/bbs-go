<script>
  import HttpClient from '../../apis/HttpClient'

  export default {
    name: "List",
    data() {
      return {
        results: [],
        listLoading: false,
        page: {},
        filters: {},
        selectedRows: [],

        addForm: {
          
          'userId': '',
          
          'ruleId': '',
          
          'linkId': '',
          
          'title': '',
          
          'summary': '',
          
          'content': '',
          
          'contentType': '',
          
          'sourceUrl': '',
          
          'sourceId': '',
          
          'sourceUrlMd5': '',
          
          'sourceTitleMd5': '',
          
          'status': '',
          
          'articleId': '',
          
          'createTime': '',
          
        },
        addFormVisible: false,
        addLoading: false,

        editForm: {
          'id': '',
          
          'userId': '',
          
          'ruleId': '',
          
          'linkId': '',
          
          'title': '',
          
          'summary': '',
          
          'content': '',
          
          'contentType': '',
          
          'sourceUrl': '',
          
          'sourceId': '',
          
          'sourceUrlMd5': '',
          
          'sourceTitleMd5': '',
          
          'status': '',
          
          'articleId': '',
          
          'createTime': '',
          
        },
        editFormVisible: false,
        editLoading: false,
      }
    },
    mounted() {
      this.list();
    },
    methods: {
      list() {
        let me = this
        me.listLoading = true
		let params = Object.assign(me.filters, {
          page: me.page.page,
          limit: me.page.limit
        })
        HttpClient.post('/api/admin/collect-article/list', params)
          .then(data => {
            me.results = data.results
            me.page = data.page
          })
          .finally(() => {
            me.listLoading = false
          })
      },
      handlePageChange (val) {
        this.page.page = val
        this.list()
      },
      handleLimitChange (val) {
        this.page.limit = val
        this.list()
      },
      handleAdd() {
        this.addForm = {
          name: '',
          description: '',
        }
        this.addFormVisible = true
      },
      addSubmit() {
        let me = this
        HttpClient.post('/api/admin/collect-article/create', this.addForm)
          .then(data => {
            me.$message({message: '提交成功', type: 'success'});
            me.addFormVisible = false
            me.list()
          })
          .catch(rsp => {
            me.$notify.error({title: '错误', message: rsp.message})
          })
      },
      handleEdit(index, row) {
        let me = this
        HttpClient.get('/api/admin/collect-article/' + row.id)
          .then(data => {
            me.editForm = Object.assign({}, data);
            me.editFormVisible = true
          })
          .catch(rsp => {
            me.$notify.error({title: '错误', message: rsp.message})
          })
      },
      editSubmit() {
        let me = this
        HttpClient.post('/api/admin/collect-article/update', me.editForm)
          .then(data => {
            me.list()
            me.editFormVisible = false
          })
          .catch(rsp => {
            me.$notify.error({title: '错误', message: rsp.message})
          })
      },

      handleSelectionChange(val) {
        this.selectedRows = val
      },
    }
  }
</script>

